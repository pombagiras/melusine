function getNormalizedName(name) {
    if (name === "Cigana") return "Pombagira Cigana";
    if (name === "Da Figueira") return "Pombagira da Figueira";
    if (name === "Da Praia") return "Pombagira da Praia";
    if (name === "Menina") return "Pombagira Menina";
    return name;
}

