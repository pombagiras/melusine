function normalizeFileName(name) {
    return name
        .toLowerCase()
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "") // remove accents
        .replace(/ – /g, "-")
        .replace(/ /g, "-");
}

// Elementos de Controle do Modal
const modal = document.getElementById('pombagiraModal');
const modalTitle = document.getElementById('modalTitle');
const modalPhrase = document.getElementById('modalPhrase');
const modalDesc = document.getElementById('modalDesc');
const modalSpecsGrid = document.getElementById('modalSpecsGrid');
const modalArticleLink = document.getElementById('modalArticleLink');

function openModal(name) {
    if (!modal) return;
    
    const normalizedKey = getNormalizedName(name);
    const data = pombagirasData[normalizedKey];
    if (!data) return;

    if (modalTitle) modalTitle.textContent = data.nome;
    if (modalPhrase) modalPhrase.textContent = data.frase ? `“${data.frase}”` : "";
    if (modalDesc) modalDesc.textContent = data.descricao;

    if (modalSpecsGrid) {
        modalSpecsGrid.innerHTML = `
            <div class="modal-spec-item"><strong>♚ Oferenda:</strong> ${data.oferenda}</div>
            <div class="modal-spec-item"><strong>♚ Cores:</strong> ${data.cores}</div>
            <div class="modal-spec-item"><strong>♚ Velas:</strong> ${data.velas}</div>
            <div class="modal-spec-item"><strong>♚ Local de Oferenda:</strong> ${data.local}</div>
            <div class="modal-spec-item"><strong>♚ Horário Ideal:</strong> ${data.horario}</div>
            <div class="modal-spec-item"><strong>♚ Lua Ideal:</strong> ${data.lua}</div>
            <div class="modal-spec-item"><strong>♚ Regência / Afinação:</strong> ${data.signo}</div>
            <div class="modal-spec-item"><strong>♚ Dia de Força:</strong> ${data.dia}</div>
        `;
    }

    if (modalArticleLink) {
        // Gera link local direto para a página estática do artigo correspondente!
        modalArticleLink.href = `guardias/${normalizeFileName(normalizedKey)}.html`;
    }

    modal.classList.add('active');
}

// Fechamento de Modal
const modalClose = document.querySelector('.modal-close');
if (modalClose) {
    modalClose.addEventListener('click', () => {
        modal.classList.remove('active');
    });
}

window.addEventListener('click', (e) => {
    if (e.target === modal) {
        modal.classList.remove('active');
    }
});

// Botão "Voltar ao Topo"
const backToTopButton = document.getElementById('backToTopBtn');
if (backToTopButton) {
    window.addEventListener('scroll', () => {
        if (window.scrollY > 300) {
            backToTopButton.classList.add('show');
        } else {
            backToTopButton.classList.remove('show');
        }
    });

    backToTopButton.addEventListener('click', () => {
        window.scrollTo({
            top: 0,
            behavior: 'smooth'
        });
    });
}
